function YPred=prediction(XX,Y1)
options = statset('UseParallel',true);
t = templateSVM(...
    'KernelFunction', 'polynomial', ...
    'PolynomialOrder', 3, ...
    'KernelScale', 'auto', ...
    'BoxConstraint', 1, ...
    'Standardize', true);
Md1 = fitcecoc(XX',Y1,'Learners',t,'Coding', 'onevsone','Options',options);
CVMd1=crossval(Md1,'KFold',10,'Options',options);
loss=1-kfoldLoss(CVMd1);
YPred = kfoldPredict(CVMd1);
